#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return resultsArray.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"ResultsTableViewController" forIndexPath:indexPath];
    
    NSDictionary *dictionary = resultsArray[indexPath.row];
    
    [cell.textLabel setText: [NSString stringWithFormat:@"%@ - %@", dictionary[DateKey], dictionary[ResultKey]]];
    
    return cell;
}


